import axios from 'axios'
import history from '../history'

/**
 * ACTION TYPES
 */
const GET_USER = 'GET_USER'
const REMOVE_USER = 'REMOVE_USER'
const REMOVE_USER_SKILL = 'REMOVE_USER_SKILL'

/**
 * INITIAL STATE
 */
const defaultUser = {}

/**
 * ACTION CREATORS
 */
const getUser = user => ({type: GET_USER, user})
const removeUser = () => ({type: REMOVE_USER})
export const removeUserSkill = skillId => ({type: REMOVE_USER_SKILL, skillId})

/**
 * THUNK CREATORS
 */
export const me = () => async dispatch => {
  try {
    const res = await axios.get('/auth/me')
    dispatch(getUser(res.data || defaultUser))
  } catch (err) {
    console.error(err)
  }
}

export const auth = (email, password, method) => async dispatch => {
  let res
  try {
    res = await axios.post(`/auth/${method}`, {email, password})
  } catch (authError) {
    return dispatch(getUser({error: authError}))
  }

  try {
    dispatch(getUser(res.data))
    history.push('/home')
  } catch (dispatchOrHistoryErr) {
    console.error(dispatchOrHistoryErr)
  }
}

export const logout = () => async dispatch => {
  try {
    await axios.post('/auth/logout')
    dispatch(removeUser())
    history.push('/login')
  } catch (err) {
    console.error(err)
  }
}

export const updateUser = user => async dispatch => {
  const currentUser = await axios.put(`/api/users/${user.userId}/profile`, user)
  dispatch(getUser(currentUser.data))
}

export const getCurrentUser = id => async dispatch => {
  const currentUser = await axios.get(`/api/users/${id}/profile`)
  dispatch(getUser(currentUser.data))
}

/**
 * REDUCER
 */
export default function(state = defaultUser, action) {
  switch (action.type) {
    case GET_USER:
      return action.user
    case REMOVE_USER:
      return defaultUser
    case REMOVE_USER_SKILL:
      return {
        ...state,
        skills: state.skills.filter(skill => skill.id !== action.skillId)
      }
    default:
      return state
  }
}
